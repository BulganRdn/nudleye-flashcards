async function main() {
  const users = await prisma.user.findMany({
    orderBy: { createdAt: "asc" },
    select: { id: true, name: true, email: true },
  });

  if (users.length === 0) {
    throw new Error("Багц эзэмшүүлэх бүртгэлтэй хэрэглэгч database-д алга байна.");
  }

  await prisma.$transaction(async (tx) => {
    await tx.deck.deleteMany();

    for (const [index, deck] of decks.entries()) {
      const owner = users[index % users.length];
      await tx.deck.create({
        data: {
          name: deck.name,
          description: deck.description,
          emoji: "NU",
          isPublic: true,
          authorId: owner.id,
          cards: {
            create: deck.cards.map(([front, back], cardIndex) => ({
              front,
              back,
              dueDate: new Date(Date.now() + cardIndex * 60_000),
            })),
          },
          progress: {
            create: {
              userId: owner.id,
              total: deck.cards.length,
              mastered: 0,
              streak: 0,
            },
          },
        },
      });
    }
  });

  const assignments = decks.map((deck, index) => {
    const owner = users[index % users.length];
    return `${deck.name} -> ${owner.name || owner.email}`;
  });

  console.log(`Replaced all decks with ${decks.length} Korean mock decks.`);
  console.log(assignments.join("\n"));
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
